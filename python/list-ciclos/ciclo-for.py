# Ejemplo 1: Iterando a través de una lista de números
numbers = [1, 2, 3, 4, 5]
for num in numbers:
  print(f"El número actual es: {num}")

# Ejemplo 2: Usando range() para crear una secuencia
# Esto imprimirá números del 0 al 4
for i in range(5):
  print(f"Iteración {i}")

# Ejemplo 3: Iterando a través de una cadena de texto
message = "Hello"
for letter in message:
  print(f"Letra: {letter}")

